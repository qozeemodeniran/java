package switching;

public class Switch {
	public static void main(String[] args) {
		String color = "";
		switch(color) {
		case "blue":
			System.out.println("You are a blue high lobber");
			break;
		case "black":
			System.out.println("You are a black high lobber");
			break;
		case "yellow":
			System.out.println("You are a rugged Nyben!!!");
			break;
		default:
			System.out.println("You are a fukcing lobber");
		}
	}
}
