package datatypes;

public class Datatypes {
	public static void main(String... args) {
		
/*
 * --------------------BYTES---------------
 * - can store integer values from -128 to 127.
 * - this data type can be used in order to save memory if it is certain that value will no exceed the bars (-128 and 127)
 * */
		byte numberByte = 100;
		System.out.println(numberByte);
		
/* ------------------SHORT--------------------
 * The short data type can store whole numbers from -32768 to 32767
 * */
		short numberShort = 5000;
		System.out.println(numberShort);
		
		
	}
}
